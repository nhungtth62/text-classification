def covert_unicode(s):
    dicchar = loaddicchar()
    return re.sub(
        r'à|á|ả|ã|ạ|ầ|ấ|ẩ|ẫ|ậ|ằ|ắ|ẳ|ẵ|ặ|è|é|ẻ|ẽ|ẹ|ề|ế|ể|ễ|ệ|ì|í|ỉ|ĩ|ị|ò|ó|ỏ|õ|ọ|ồ|ố|ổ|ỗ|ộ|ờ|ớ|ở|ỡ|ợ|ù|ú|ủ|ũ|ụ|ừ|ứ|ử|ữ|ự|ỳ|ý|ỷ|ỹ|ỵ|À|Á|Ả|Ã|Ạ|Ầ|Ấ|Ẩ|Ẫ|Ậ|Ằ|Ắ|Ẳ|Ẵ|Ặ|È|É|Ẻ|Ẽ|Ẹ|Ề|Ế|Ể|Ễ|Ệ|Ì|Í|Ỉ|Ĩ|Ị|Ò|Ó|Ỏ|Õ|Ọ|Ồ|Ố|Ổ|Ỗ|Ộ|Ờ|Ớ|Ở|Ỡ|Ợ|Ù|Ú|Ủ|Ũ|Ụ|Ừ|Ứ|Ử|Ữ|Ự|Ỳ|Ý|Ỷ|Ỹ|Ỵ',
        lambda x: dicchar[x.group()], s)

# Xóa dấu trong câu
def remove_special_character(s):
    # Xóa dấu chấm, phẩy, hỏi ở cuối câu
    s = re.sub(r"[\.,\?]+$-", "", s)
    # Xóa tất cả dấu chấm, phẩy, chấm phẩy, chấm thang, ... trong câu
    special_characters = ['!', '@', '#', '$', '%', '^', '&', '*', '(', ')', '_', '-', '+','=', '{', '}', '[', ']', '|', '\\', ':', ';', '"', "'", '<', '>', ',', '.', '?', '/']

    # s = s.translate(None, ''.join(special_characters))
    s = s.translate(str.maketrans('','', ''.join(special_characters)))
    s = s.replace("  ", ' ')
    s = s.strip().lower()
    return s

    
def chuan_hoa_cau(sentence):
    result = covert_unicode(sentence)
    result = remove_special_character(result)
    result = chuan_hoa_dau_cau_tieng_viet(result)
    return result

